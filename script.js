// ===== i18n Engine =====
let currentLang = "en";

function t(key) {
  return translations[currentLang]?.[key] ?? translations["en"][key] ?? key;
}

function applyTranslations(lang) {
  // Text content
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const val = translations[lang]?.[key] ?? translations["en"][key];
    if (val !== undefined) el.textContent = val;
  });

  // HTML content (for elements with inline markup like <span>, <strong>)
  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const key = el.getAttribute("data-i18n-html");
    const val = translations[lang]?.[key] ?? translations["en"][key];
    if (val !== undefined) el.innerHTML = val;
  });

  // Title (tooltip) attributes
  document.querySelectorAll("[data-i18n-title]").forEach((el) => {
    const key = el.getAttribute("data-i18n-title");
    const val = translations[lang]?.[key] ?? translations["en"][key];
    if (val !== undefined) el.setAttribute("title", val);
  });

  // Update html lang attribute
  document.documentElement.setAttribute("lang", lang);

  // Update theme button text with correct language
  const theme = document.documentElement.getAttribute("data-theme") || "dark";
  const themeTextEl = document.getElementById("themeText");
  if (themeTextEl) {
    themeTextEl.textContent =
      theme === "light"
        ? (translations[lang]?.["theme.light"] ?? "Light")
        : (translations[lang]?.["theme.dark"] ?? "Dark");
  }

  // Update lang button active states
  const btnEn = document.getElementById("langBtnEn");
  const btnHu = document.getElementById("langBtnHu");
  if (btnEn) {
    btnEn.classList.toggle("active", lang === "en");
    btnEn.setAttribute("aria-pressed", String(lang === "en"));
  }
  if (btnHu) {
    btnHu.classList.toggle("active", lang === "hu");
    btnHu.setAttribute("aria-pressed", String(lang === "hu"));
  }
}

function getLanguage() {
  const params = new URLSearchParams(window.location.search);
  const urlLang = params.get("lang");
  if (urlLang === "hu" || urlLang === "en") return urlLang;
  const saved = localStorage.getItem("lang");
  if (saved === "hu" || saved === "en") return saved;
  return "en";
}

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem("lang", lang);

  // Update URL query param without page reload
  const url = new URL(window.location.href);
  if (lang === "en") {
    url.searchParams.delete("lang");
  } else {
    url.searchParams.set("lang", lang);
  }
  history.pushState({ lang }, "", url);

  applyTranslations(lang);
}

