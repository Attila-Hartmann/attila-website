const menuBtn = document.getElementById("menuBtn");
const mobileNav = document.getElementById("mobileNav");

function toggleMenu() {
  const isOpen = mobileNav.classList.toggle("open");
  menuBtn.setAttribute("aria-expanded", String(isOpen));
  mobileNav.setAttribute("aria-hidden", String(!isOpen));
}

// ===== Theme Toggle =====
const themeBtn = document.getElementById("themeBtn");
const themeIcon = document.getElementById("themeIcon");
const themeText = document.getElementById("themeText");

function setTheme(theme) {
  document.documentElement.setAttribute("data-theme", theme);
  localStorage.setItem("theme", theme);

  if (themeIcon && themeText) {
    if (theme === "light") {
      themeIcon.textContent = "☀️";
      themeText.textContent = t("theme.light");
    } else {
      themeIcon.textContent = "🌙";
      themeText.textContent = t("theme.dark");
    }
  }
}

function getPreferredTheme() {
  const saved = localStorage.getItem("theme");
  if (saved === "dark" || saved === "light") return saved;
  return window.matchMedia &&
    window.matchMedia("(prefers-color-scheme: light)").matches
    ? "light"
    : "dark";
}

// ===== Initialise =====
// Language must be set before theme so theme text is in correct language
currentLang = getLanguage();
setTheme(getPreferredTheme());
applyTranslations(currentLang);

// ===== Event Listeners =====
if (themeBtn) {
  themeBtn.addEventListener("click", () => {
    const current =
      document.documentElement.getAttribute("data-theme") || "dark";
    setTheme(current === "dark" ? "light" : "dark");
  });
}

if (menuBtn && mobileNav) {
  menuBtn.addEventListener("click", toggleMenu);

  mobileNav.querySelectorAll("a").forEach((a) =>
    a.addEventListener("click", () => {
      mobileNav.classList.remove("open");
      menuBtn.setAttribute("aria-expanded", "false");
      mobileNav.setAttribute("aria-hidden", "true");
    }),
  );
}

document
  .getElementById("langBtnEn")
  ?.addEventListener("click", () => setLanguage("en"));
document
  .getElementById("langBtnHu")
  ?.addEventListener("click", () => setLanguage("hu"));

// Handle browser back/forward navigation
window.addEventListener("popstate", (e) => {
  const lang = e.state?.lang ?? getLanguage();
  currentLang = lang;
  applyTranslations(lang);
});
